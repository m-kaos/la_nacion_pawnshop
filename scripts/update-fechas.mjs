function toISO(str) {
  const [m, d, y] = str.split('/').map(Number);
  return new Date(y, m - 1, d, 12, 0, 0).toISOString();
}

// ─── Auth ────────────────────────────────────────────────────────────────────
let bearerToken = null;

async function gql(query, variables = {}) {
  const headers = { 'Content-Type': 'application/json' };
  if (bearerToken) headers['Authorization'] = `Bearer ${bearerToken}`;

  const res = await fetch(ADMIN_API, {
    method: 'POST',
    headers,
    body: JSON.stringify({ query, variables }),
  });

  const token = res.headers.get('vendure-auth-token');
  if (token) bearerToken = token;

  const json = await res.json();
  if (json.errors) throw new Error(JSON.stringify(json.errors));
  return json.data;
}

async function login() {
  await gql(`mutation { login(username: "${USERNAME}", password: "${PASSWORD}") { ... on CurrentUser { id } } }`);
  if (!bearerToken) throw new Error('Auth failed');
  console.log('✅ Logged in');
}

// ─── Main ─────────────────────────────────────────────────────────────────────
async function main() {
  console.log(`\n🚀 Connecting to ${ADMIN_API}...\n`);
  await login();

  // Fetch all products (with SKUs)
  const data = await gql(`{
    products(options: { take: 500 }) {
      items { id variants { sku } }
    }
  }`);

  const products = data.products.items;
  console.log(`📦 Found ${products.length} products\n`);

  let updated = 0, skipped = 0;

  for (const product of products) {
    const sku = product.variants?.[0]?.sku;
    const fechaStr = FECHAS[sku];

    if (!fechaStr) {
      skipped++;
      continue;
    }

    const fechaISO = toISO(fechaStr);

    try {
      await gql(`
        mutation UpdateProduct($input: UpdateProductInput!) {
          updateProduct(input: $input) { id }
        }
      `, {
        input: {
          id: product.id,
          customFields: { fechaEntrada: fechaISO },
        },
      });
      updated++;
      process.stdout.write(`✅ ${sku} → ${fechaStr}\n`);
    } catch (e) {
      process.stdout.write(`❌ ${sku} — ${e.message.substring(0, 60)}\n`);
    }

    await new Promise(r => setTimeout(r, 100));
  }

  console.log(`\n🎉 Done! ${updated} updated, ${skipped} skipped (no date mapping).`);
}

main().catch(console.error);
