// ─── GraphQL helpers ────────────────────────────────────────────────────────
let bearerToken = null;

async function gql(query, variables = {}) {
  const headers = { 'Content-Type': 'application/json' };
  if (bearerToken) headers['Authorization'] = `Bearer ${bearerToken}`;

  const res = await fetch(ADMIN_API, {
    method: 'POST',
    headers,
    body: JSON.stringify({ query, variables }),
  });

  // Vendure sends auth token in this header on login
  const token = res.headers.get('vendure-auth-token');
  if (token) bearerToken = token;

  const json = await res.json();
  if (json.errors) throw new Error(JSON.stringify(json.errors));
  return json.data;
}

async function login() {
  const data = await gql(`
    mutation { login(username: "${USERNAME}", password: "${PASSWORD}") {
      ... on CurrentUser { id identifier }
      ... on InvalidCredentialsError { message }
    }}
  `);
  if (!bearerToken) throw new Error('No auth token received — check credentials');
  console.log('✅ Logged in as:', data.login.identifier);
}

async function getDefaultIds() {
  const data = await gql(`{
    taxCategories { items { id name } }
    channels { items { id code defaultCurrencyCode } }
  }`);
  const taxCategory = data.taxCategories.items[0];
  const channel = data.channels.items[0];
  console.log(`📦 Tax category: ${taxCategory.name} (${taxCategory.id})`);
  console.log(`📦 Channel: ${channel.code} (${channel.id})`);
  return { taxCategoryId: taxCategory.id, channelId: channel.id };
}

function slugify(text, sku) {
  return sku + '-' + text.toLowerCase()
    .replace(/[áàä]/g, 'a').replace(/[éèë]/g, 'e')
    .replace(/[íìï]/g, 'i').replace(/[óòö]/g, 'o')
    .replace(/[úùü]/g, 'u').replace(/ñ/g, 'n')
    .replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '').substring(0, 80);
}

async function createArticulo(item, taxCategoryId) {
  const [familia, sku, contrato, descripcion, marca, modelo, serie, costo, precio] = item;

  // 1. Create product
  const createData = await gql(`
    mutation CreateProduct($input: CreateProductInput!) {
      createProduct(input: $input) { id }
    }
  `, {
    input: {
      translations: [{ languageCode: 'es', name: descripcion, slug: slugify(descripcion, sku), description: '' }],
      customFields: {
        marca: marca === '1' ? '' : marca,
        modelo: modelo === '1' || modelo === '0' ? '' : modelo,
        numeroDeSerie: serie === '1' || serie === '0' ? '' : serie,
        valorAvaluo: Math.round(costo * 100), // centavos
        estadoArticulo: 'Disponible',
        familia,
      },
    },
  });

  const productId = createData.createProduct.id;

  // 2. Create variant with price
  await gql(`
    mutation CreateVariants($input: [CreateProductVariantInput!]!) {
      createProductVariants(input: $input) { id }
    }
  `, {
    input: [{
      productId,
      sku,
      translations: [{ languageCode: 'es', name: descripcion }],
      taxCategoryId,
      price: Math.round(precio * 100), // centavos
      stockOnHand: 1,
      trackInventory: 'FALSE',
    }],
  });

  return productId;
}

// ─── Main ───────────────────────────────────────────────────────────────────
async function main() {
  console.log(`\n🚀 Connecting to ${ADMIN_API}...\n`);

  await login();
  const { taxCategoryId } = await getDefaultIds();

  console.log(`\n📋 Uploading ${RAW.length} artículos...\n`);

  let ok = 0, fail = 0;
  for (let i = 0; i < RAW.length; i++) {
    const item = RAW[i];
    const sku = item[1];
    const desc = item[3];
    try {
      await createArticulo(item, taxCategoryId);
      ok++;
      process.stdout.write(`✅ [${i + 1}/${RAW.length}] ${sku} — ${desc.substring(0, 50)}\n`);
    } catch (e) {
      fail++;
      process.stdout.write(`❌ [${i + 1}/${RAW.length}] ${sku} — ${e.message.substring(0, 80)}\n`);
    }
    // Small delay to avoid overwhelming the server
    await new Promise(r => setTimeout(r, 150));
  }

  console.log(`\n🎉 Done! ${ok} created, ${fail} failed.`);
}

main().catch(console.error);
